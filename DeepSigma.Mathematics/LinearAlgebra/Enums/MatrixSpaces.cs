namespace DeepSigma.Mathematics.LinearAlgebra.Enums;

/// <summary>
/// Enumeration to represent the different types of spaces associated with a matrix, specifically the column space and row space.
/// </summary>
public enum MatrixSpaces
{
    /// <summary>
    /// The column space of a matrix, also known as the range or image, is the set of all possible linear combinations of the columns of the matrix.
    /// </summary>
    ColumnSpace,
    /// <summary>
    /// The row space of a matrix, also known as the coimage, is the set of all possible linear combinations of the rows of the matrix.
    /// </summary>
    RowSpace,
}
